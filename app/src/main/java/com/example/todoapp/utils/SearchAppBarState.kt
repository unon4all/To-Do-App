package com.example.todoapp.utils

/**
 * Enum class representing the states of a search app bar.
 */
enum class SearchAppBarState {
    /**
     * Represents the state when the search app bar is fully opened.
     */
    OPENED,

    /**
     * Represents the state when the search app bar is fully closed.
     */
    CLOSED,

    /**
     * Represents the state when the search app bar is triggered but not fully opened.
     */
    TRIGGERED
}
